#!/bin/bash

TARGET_DIR="/Users/louis/Documents"

read -p "Are you sure you want to delete all node_modules folders in $TARGET_DIR? (y/n): " confirm

if [[ $confirm == [yY] ]]; then
    find "$TARGET_DIR" -name "node_modules" -type d -prune -exec rm -rf '{}' +
    echo "All node_modules folders in $TARGET_DIR have been deleted."
else
    echo "Operation canceled."
fi
